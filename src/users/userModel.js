const mongoose = require("mongoose");
const userSchema = new mongoose.SchemaType({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
});

const User = mongoose.model("User, userSchema")

modules.exports = User